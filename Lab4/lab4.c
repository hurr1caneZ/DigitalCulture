#include <stdio.h>

int main()
{
    int a,b;
    scanf("%d", &a); //Вводим с клавиатуры десятичное число
    printf("%d",((a >= 100) & (a <= 1000))); //Проверка диапазона
    scanf("%d", &b); //Вводим с клавиатуры десятичное число
    b = (b >> 22) % 2; //Узнаем 22 бит с конца 
    printf("%d", b); //Выводим значение 22 бита с конца
    return 0;
}